				<h2>Project: Know Thy Neighbor</h2>
				
				<p>As Christians, we are to love and serve Christ and our neighbors in the surrounding culture at the same time.  You cannot love and serve your neighbor if you do not know that neighbor. Time spent with neighbors result in conversions which may result in spiritual conversation, or at the very least a greater appreciation of the work of Christ.</p>   
				
				<h3>Radical Challenge</h3>
				<p>Getting to know our neighbors should be important to Christians.  This project is to challenge us to actually get it on our calendars and hopefully make it a habit.</p>    
				<p>Here's the challenge:</p>
				<ol>
				<li>Get to know at least 1 neighbor (one that you don't know at all or know very well) between September and December.</li>
				<li>The goal at this time is to establish a relationship but not necessarily share the Gospel (but if the opportunity presents itself to share the Gospel, by all means do it).</li>
				</ol>
				
				<p>If you would commit to this challenge, please sign-up.</p>
				
				<p>To help you in this project, here are a few tried and tested practical suggestions on how to get to know your neighbors.  Please check back regularly for updated suggestions and ideas.</p>
				<ul>
				<li><strong>Invite to coffee, lunch, or dinner.</strong></li>
				<li><strong>If you have small children, invite neighbors with small children over for a play date.</strong></li>
				<li><strong>Invite everyone.</strong> That is, invite a large group of people, either your whole apartment building or your whole block. This will avoid the impression that you want to build a clique. It gives you a much higher chance for success. And it usually just makes the evening much more enjoyable.</li>
				<li><strong>Use nice flyers or invitations.</strong> For a first get-together, you may want to use full-color flyers that had a picture of a tasteful dinner scene and the words, “We think it’s too bad we’ve never met all our neighbors.” People want to know your intentions, and they like to be invited to nice events. Do them the honor. It makes a difference.  If you don't have the skill or time for flyers and invitations, consider using <a href="http://www.thedesktopdivaonline.com/">The Desktop Diva.</a></li>
				<li><strong>Plan the get together for a Sunday.</strong> This is not an absolute, but few people have major commitments on a Sunday at 2 or 3 p.m., which means more can come and fewer have to rush off. Try to plan ahead by at least three weeks.</li>
				<li><strong>Learn how to actively listen before you invite friends and neighbors over.</strong> Not only will you not have to prepare “entertainment” for these people, but if you are truly interested in who they are and don’t squash conversation as it happens, the entertainment will take care of itself.</li>
				<li><strong>Involve any of the other neighbors you can (potluck, progressive dinner).</strong> This helps ensure that they show up, and it also means they will feel more invested. Hopefully it will keep them from thinking you are trying to be some kind of social control freak.</li>
				<li><strong>Be transparent about your faith.</strong> When you first met with your neighbors, many may be excited that you've taken such a bold step. In that moment, you can simply say: "This is something Christians value." And in that one sentence, you have made your faith and given all credit for something which the people openly liked about us to Christianity.  Plain-spoken honesty is the best, and most effective, way to live with your neighbors. Another way to be transparent about your faith is to invite your neighbors to join with you in blessing the meal.</li>
				</ul>
